# Principais métodos da classe String
print("\n===== Principais métodos da classe String =====")
str = "alan turing"
print("str => ", str)
print("\nCapitalize")
print("str.capitalize() => ", str.capitalize())

print("\nCount")
print("str.count('a') => ", str.count('a'))

print("\nStarts With")
print("str.startswith('a') => ", str.startswith('a'))

print("\nEnds with")
print("str.endswith('a') => ", str.endswith('a'))

print("\nSplit")
print("str.split() => ", str.split())

print("\nJoin")
print("' '.join(['Chico', 'Anísio']) => ", ' '.join(['Chico', 'Anísio']))
