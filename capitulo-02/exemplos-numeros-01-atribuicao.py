# Exemplos de definição dos números

# por atribuição normal
print("\n===== Atribuição ====")
print('por atribuição normal')
num1 = 1
num2 = 1. #para ser float só precisa do ponto
num3 = 1j #para ser complexo só precisa do J ou j

print(num1, type(num1))
print(num2, type(num2))
print(num3, type(num3))

# por função
print("\npor função")
num1 = int(1)
num2 = float('1') #para ser float só precisa do ponto
num3 = complex(1,2) #para ser complexo só precisa do J ou j

print(num1, type(num1))
print(num2, type(num2))
print(num3, type(num3))
